package com.example.demo.utils;

public class TitleQuestion {
    private long id;
    private String title;

    public TitleQuestion(long id, String title) {
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
